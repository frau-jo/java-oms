public void cnd(){
        try {

                 pst3 = con.prepareStatement("select ITEM_QTY from supplies where ITEM_CODE = ?");
		 pst3.setString(1,"CND");
		 rs2 = pst3.executeQuery();

            
            
            	 while(rs2.next())
            	{       
                        
                            int currentQty = Integer.parseInt(rs2.getString("ITEM_QTY"));
                            
                            int ctr_qty = POSNEW.qty_CND;
                            int ctr1= POSNEW.ctr_CND;
                            
                            
                            if(ctr1 != 0){
                                
                            if (ctr_qty == 0){
                            togbtn500.setVisible(false);
                            outofstock_Solo.setVisible(true);

                        }
                        else{
                            togbtn500.setVisible(true);
                            outofstock_Solo.setVisible(false);

                        }
                            }
                        
                            else if (ctr1 == 0){
                        
               		  if (currentQty == 0){
                            togbtn500.setVisible(false);
                            outofstock_Solo.setVisible(true);
                             POSNEW.ctr_CND = POSNEW.ctr_CND  +1;
                        }
                        else{
                            togbtn500.setVisible(true);
                            outofstock_Solo.setVisible(false);

                        }
                       
                }
               
                }

    
            
        }catch (SQLException ex) {
            Logger.getLogger(SnkCOrdering.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

