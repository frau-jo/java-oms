public void bott350(){
        try {

                 pst3 = con.prepareStatement("select ITEM_QTY from supplies where ITEM_CODE = ?");
		 pst3.setString(1,"BOTT350");
		 rs2 = pst3.executeQuery();

            
            
            	 while(rs2.next())
            	{       
                        
                            int currentQty = Integer.parseInt(rs2.getString("ITEM_QTY"));
                            
                            int ctr_qty = POSNEW.qty_bott350;
                            int ctr1= POSNEW.ctr_bott350;
                            
                            
                            if(ctr1 != 0){
                                
                            if (ctr_qty == 0){
                            togbtn500.setVisible(false);
                            outofstock_350ml.setVisible(true);

                        }
                        else{
                            togbtn500.setVisible(true);
                            outofstock_350ml.setVisible(false);

                        }
                            }
                        
                            else if (ctr1 == 0){
                        
               		  if (currentQty == 0){
                            togbtn500.setVisible(false);
                            outofstock_350ml.setVisible(true);
                             POSNEW.ctr_bott350 = POSNEW.ctr_bott350  +1;
                        }
                        else{
                            togbtn500.setVisible(true);
                            outofstock_350ml.setVisible(false);

                        }
                       
                }
               
                }

    
            
        }catch (SQLException ex) {
            Logger.getLogger(InfSOrdering.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

